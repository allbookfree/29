export function getPoolForType(type) {
  if (type === "vector") return VECTOR_POOL;
  if (type === "video") return VIDEO_POOL;
  return IMAGE_POOL;
}

export function getSubjectCount(type) {
  const pool = type ? getPoolForType(type) : IMAGE_POOL;
  return Object.values(pool).reduce((sum, arr) => sum + arr.length, 0);
}

export function getTotalSubjectCount() {
  return getSubjectCount("image") + getSubjectCount("vector") + getSubjectCount("video");
}

export function getAllSubjects(type) {
  const pool = type ? getPoolForType(type) : IMAGE_POOL;
  const all = [];
  for (const [cat, subjects] of Object.entries(pool)) {
    for (const s of subjects) {
      all.push({ category: cat, subject: s });
    }
  }
  return all;
}

export function getCategoryCount(type) {
  const pool = type ? getPoolForType(type) : IMAGE_POOL;
  return Object.keys(pool).length;
}

function pickRandom(arr, n) {
  const shuffled = [...arr];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled.slice(0, n);
}

export function getRandomSubjects(type, usedCategories = {}, usedSubjectKeys = new Set(), count = 1) {
  const pool = getPoolForType(type);
  const combinators = CONTEXT_COMBINATORS[type] || CONTEXT_COMBINATORS.image;
  const catEntries = Object.entries(pool);

  const catUsage = catEntries.map(([cat, subjects]) => ({
    category: cat,
    subjects,
    used: usedCategories[cat] || 0,
  }));
  catUsage.sort((a, b) => a.used - b.used);

  const results = [];
  const usedInBatch = new Set();

  for (let i = 0; i < count; i++) {
    const leastUsed = catUsage[0].used;
    const leastUsedCats = catUsage.filter(c => c.used <= leastUsed + 1);
    const chosen = leastUsedCats[Math.floor(Math.random() * leastUsedCats.length)];

    let available = chosen.subjects.filter(s => {
      const key = `${chosen.category}::${s}`;
      return !usedInBatch.has(key) && !usedSubjectKeys.has(key);
    });

    if (available.length === 0) {
      available = chosen.subjects.filter(s => !usedInBatch.has(`${chosen.category}::${s}`));
    }
    if (available.length === 0) {
      available = chosen.subjects;
    }

    const subject = available[Math.floor(Math.random() * available.length)];
    const context = combinators[Math.floor(Math.random() * combinators.length)];
    const subjectKey = `${chosen.category}::${subject}`;

    results.push({
      category: chosen.category,
      subject,
      context,
      fullConcept: `${subject} — ${context}`,
      subjectKey,
    });
    usedInBatch.add(subjectKey);
    chosen.used++;
    catUsage.sort((a, b) => a.used - b.used);
  }

  return results;
}
