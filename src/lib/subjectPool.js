export const CATEGORY_NAMES = Object.keys(HALAL_CATEGORIES);

export function getSubjectCount() {
  return Object.values(HALAL_CATEGORIES).reduce((sum, arr) => sum + arr.length, 0);
}

export function getAllSubjects() {
  const all = [];
  for (const [cat, subjects] of Object.entries(HALAL_CATEGORIES)) {
    for (const s of subjects) {
      all.push({ category: cat, subject: s });
    }
  }
  return all;
}

export function getRandomSubjects(usedCategories = {}, count = 1) {
  const catEntries = Object.entries(HALAL_CATEGORIES);
  const catUsage = catEntries.map(([cat, subjects]) => ({
    category: cat,
    subjects,
    used: usedCategories[cat] || 0,
  }));
  catUsage.sort((a, b) => a.used - b.used);

  const results = [];
  const usedInBatch = new Set();

  for (let i = 0; i < count; i++) {
    const leastUsed = catUsage[0].used;
    const leastUsedCats = catUsage.filter(c => c.used <= leastUsed + 1);
    const chosen = leastUsedCats[Math.floor(Math.random() * leastUsedCats.length)];

    const available = chosen.subjects.filter(s => !usedInBatch.has(`${chosen.category}:${s}`));
    const subject = available.length > 0
      ? available[Math.floor(Math.random() * available.length)]
      : chosen.subjects[Math.floor(Math.random() * chosen.subjects.length)];

    results.push({ category: chosen.category, subject });
    usedInBatch.add(`${chosen.category}:${subject}`);
    chosen.used++;
    catUsage.sort((a, b) => a.used - b.used);
  }

  return results;
}
