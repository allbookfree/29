function pickRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function getRandomSeeds(count = 1) {
  const results = [];
  const usedPhrases = new Set();

  for (let i = 0; i < count; i++) {
    let noun, adjective, context, phrase;
    let attempts = 0;
    do {
      noun = pickRandom(SEED_NOUNS);
      adjective = pickRandom(SEED_ADJECTIVES);
      context = pickRandom(SEED_CONTEXTS);
      phrase = `${adjective} ${noun}`;
      attempts++;
    } while (usedPhrases.has(phrase) && attempts < 20);

    usedPhrases.add(phrase);
    results.push({
      noun,
      adjective,
      context,
      seedPhrase: `${adjective} ${noun} — ${context}`,
    });
  }
  return results;
}

export function getSeedStats() {
  const totalNouns = SEED_NOUNS.length;
  const totalAdjectives = SEED_ADJECTIVES.length;
  const totalContexts = SEED_CONTEXTS.length;
  const totalCombinations = totalNouns * totalAdjectives * totalContexts;
  return { totalNouns, totalAdjectives, totalContexts, totalCombinations };
}
